class LLMClient:
    """
    OpenRouter unified client (replaces Gemini/OpenAI/Anthropic)
    """

    MODEL = "openai/gpt-oss-120b:free"
    # Faster alternative:
    # MODEL = "meta-llama/llama-3.1-70b-instruct:free"

    def __init__(self):
        from openai import OpenAI
        self.client = OpenAI(
            base_url="https://openrouter.ai/api/v1",
            api_key=os.environ.get("OPENROUTER_API_KEY", "")
        )

    def call(self, prompt: str, max_tokens: int = 4096) -> str:
        max_tokens = min(max_tokens, 2500)
        try:
            resp = self.client.chat.completions.create(
                model=self.MODEL,
                temperature=0.2,
                max_tokens=max_tokens,
                messages=[{"role": "user", "content": prompt}]
            )
            return resp.choices[0].message.content or ""
        except Exception as e:
            print("[OpenRouter Error]", e)
            return ""

    def call_json(self, prompt: str, max_tokens: int = 4096):
        prompt += "\n\nIMPORTANT: Output STRICT VALID JSON ONLY."
        raw = self.call(prompt, max_tokens)
        raw = re.sub(r"```json\s*", "", raw)
        raw = re.sub(r"```", "", raw).strip()
        try:
            return json.loads(raw)
        except:
            match = re.search(r"(\{.*\}|\[.*\])", raw, re.DOTALL)
            if match:
                try:
                    return json.loads(match.group(1))
                except:
                    pass
        print("[JSON FAIL]", raw[:300])
        return {}



