class PDFParser:
    DATE_FMTS = ["%b %d, %Y %I:%M %p", "%d %b %Y", "%d-%b-%Y",
                 "%d/%m/%Y", "%b %d, %Y", "%Y-%m-%d", "%d %b %y"]

    GENERIC_PATTERNS = [
        r"(\d{1,2}[\s\-/]\w{3}[\s\-/]\d{2,4})\s+(.+?)\s+([\d,]+\.\d{2})\s*(Dr|Cr)?",
        r"(\d{2}/\d{2}/\d{4})\s+(.+?)\s+([\d,]+\.\d{2})\s*(Dr|Cr)?",
        r"(\d{4}-\d{2}-\d{2})\s+(.+?)\s+[-]?([\d,]+\.\d{2})",
    ]

    def parse(self, path: str) -> pd.DataFrame:
        rows = []
        try:
            with pdfplumber.open(path) as pdf:
                full_text = ""
                for page in pdf.pages:
                    full_text += (page.extract_text() or "") + "\n"

                phonepe_pat = re.compile(
                    r"(\w{3}\s+\d{2},\s+\d{4}\s+\d{1,2}:\d{2})"
                    r"(?:\s+(?:AM|PM))?"
                    r"\s+(?:Paid to|Received from)\s+"
                    r"(.+?)"
                    r"\s+Transaction ID:.*?"
                    r"(Debit|Credit)"
                    r"\s+INR\s+([\d,]+\.\d{2})",
                    re.DOTALL
                )
                for m in phonepe_pat.finditer(full_text):
                    date_str, merchant, txn_type, amount_str = m.groups()
                    if txn_type != "Debit":
                        continue
                    try:
                        date = datetime.strptime(date_str.strip(), "%b %d, %Y %H:%M")
                        amt  = float(amount_str.replace(",", ""))
                        if amt > 0:
                            rows.append({
                                "date":     date,
                                "merchant": merchant.strip()[:80],
                                "amount":   amt,
                                "month":    date.strftime("%b %Y")
                            })
                    except Exception:
                        pass

                if not rows:
                    for page in pdf.pages:
                        text = page.extract_text() or ""
                        for pat in self.GENERIC_PATTERNS:
                            for m in re.finditer(pat, text, re.IGNORECASE | re.DOTALL):
                                r = self._from_generic_match(m)
                                if r: rows.append(r)
                        for tbl in (page.extract_tables() or []):
                            rows += self._from_table(tbl)

        except Exception as e:
            print(f"[PDF] {e}")

        if not rows:
            return pd.DataFrame()
        df = pd.DataFrame(rows).drop_duplicates(subset=["date","merchant","amount"])
        print(f"[PDF] Extracted {len(df)} debit transactions")
        return df

    def _from_generic_match(self, m):
        try:
            g = m.groups()
            date = self._parse_date(g[0])
            if not date: return None
            amt_str = next((x for x in g[1:] if x and re.match(r"[\d,]+\.?\d*$", x.replace(",",""))), None)
            if not amt_str: return None
            amt  = float(amt_str.replace(",",""))
            desc = g[1].strip() if g[1] != amt_str else (g[2].strip() if len(g) > 2 else "")
            if amt <= 0 or not desc: return None
            return {"date": date, "merchant": desc[:80],
                    "amount": amt, "month": date.strftime("%b %Y")}
        except Exception:
            return None

    def _from_table(self, table):
        rows = []
        for row in table:
            if not row or len(row) < 3: continue
            cells = [str(c or "").strip() for c in row]
            di = next((i for i, c in enumerate(cells) if self._parse_date(c)), None)
            ai = next((i for i, c in enumerate(cells)
                       if re.match(r"^[\d,]+\.?\d*$", c.replace(",",""))), None)
            if di is None or ai is None: continue
            desc = " ".join(cells[i] for i in range(len(cells)) if i not in (di, ai) and cells[i])[:60]
            date = self._parse_date(cells[di])
            try:
                amt = float(cells[ai].replace(",",""))
                if amt > 0 and date:
                    rows.append({"date": date, "merchant": desc,
                                 "amount": amt, "month": date.strftime("%b %Y")})
            except ValueError:
                pass
        return rows

    def _parse_date(self, s):
        s = re.sub(r"\s+", " ", s.strip())
        for fmt in self.DATE_FMTS:
            try: return datetime.strptime(s, fmt)
            except ValueError: pass
        return None


