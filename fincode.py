class AICore:

    def __init__(self, llm: LLMClient):
        self.llm = llm

    # ── 3A: Subscription Detection ────────────────────────────
    def detect_subscriptions(self, df: pd.DataFrame) -> list:
        """
        Technique: Context stuffing + chunking + few-shot + CoT
        Summarises all merchants then feeds to LLM in chunks of 80.
        LLM decides what is/isn't a subscription and returns structured JSON.
        """
        summary = (
            df.groupby("merchant")
            .agg(count=("amount","count"), avg=("amount","mean"),
                 min_amt=("amount","min"), max_amt=("amount","max"),
                 first_date=("date","min"), last_date=("date","max"),
                 all_amounts=("amount", list), all_dates=("date", list))
            .reset_index()
        )

        rows = []
        for _, r in summary.iterrows():
            dates_ts = sorted(r["all_dates"])
            # compute gap using timestamps
            if len(dates_ts) > 1:
                gaps = [(dates_ts[i]-dates_ts[i-1]).days for i in range(1,len(dates_ts))]
                avg_gap = round(sum(gaps)/len(gaps),1)
            else:
                avg_gap = None

            # convert to string ONLY for LLM
            dates_str = [d.strftime("%Y-%m-%d") for d in dates_ts]

            rows.append({
                "merchant": r["merchant"],
                "count": int(r["count"]),
                "avg_amount": round(float(r["avg"]), 2),
                "min_amount": round(float(r["min_amt"]), 2),
                "max_amount": round(float(r["max_amt"]), 2),
                "first_date": dates_str[0],
                "last_date": dates_str[-1],
                "avg_gap_days": avg_gap,
                "all_amounts": [round(float(a),2) for a in r["all_amounts"]],
                "all_dates": dates_str
            })

        all_subs = []
        for i in range(0, len(rows), 10):
          all_subs.extend(self._detect_chunk(rows[i:i+10]))
        return all_subs

    def _detect_chunk(self, chunk: list) -> list:
        prompt = f"""{PERSONA}

TASK: Analyse these merchant transaction summaries from an Indian user's bank/UPI statement.
Identify which are RECURRING SUBSCRIPTIONS vs one-time purchases.

TECHNIQUE — Chain of Thought:
For each merchant reason:
  1. avg_gap_days ≈ 28–31 → monthly
  2. avg_gap_days ≈ 365 → yearly
  3. avg_gap_days < 10 → weekly/daily
  If avg_gap_days exists, trust it strongly.
  4. Is amount stable or wildly variable?
  5. Is this a known Indian or global subscription service?
Then set is_subscription and confidence.

FEW-SHOT EXAMPLE:
Input:  {{"merchant":"Netflix","count":6,"avg_amount":649.0,"all_dates":["2024-01-15","2024-02-15","2024-03-15","2024-04-15","2024-05-15","2024-06-15"]}}
Output: {{"merchant":"Netflix","is_subscription":true,"billing_cycle":"monthly","category":"OTT","avg_amount":649.0,"monthly_cost":649.0,"annual_cost":7788.0,"next_renewal":"2024-07-15","confidence":98,"confidence_reason":"Charges exactly INR 649 on the 15th of every month for 6 months."}}

Input:  {{"merchant":"Amazon","count":1,"avg_amount":2499.0,"all_dates":["2024-03-10"]}}
Output: {{"merchant":"Amazon","is_subscription":false}}

TRANSACTION DATA:
{json.dumps(chunk, indent=2)}

RULES:
- Return ONLY a JSON array
- Include merchants if confidence >= 25
- No markdown, no explanation

OUTPUT SCHEMA:
Return MINIMAL JSON. Do NOT include explanations.

[{{
  "merchant": "string",
  "billing_cycle": "weekly|monthly|quarterly|annual",
  "category": "OTT|SaaS|Fitness|News|Cloud|Food-Delivery|Telecom|Other",
  "monthly_cost": 0.0,
  "annual_cost": 0.0,
  "next_renewal": "YYYY-MM-DD",
  "confidence": 0
}}]"""

        result = self.llm.call_json(prompt, max_tokens=4096)
        return result if isinstance(result, list) else []

    # ── 3B: Behavioural Analysis ──────────────────────────────
    def analyse_behaviour(self, subs: list) -> list:
        """
        Technique: CoT + structured output
        LLM inspects each subscription's amount history and date gaps
        to flag: forgotten, price hike, duplicates, trial traps.
        """
        if not subs: return subs

        prompt = f"""{PERSONA}

TASK: Perform behavioural analysis on these detected subscriptions.

TECHNIQUE — Chain of Thought for each subscription:
  1. Look at all_amounts — stable? increasing? irregular?
  2. Look at all_dates — when was last charge? How many days ago?
  3. Compare subscriptions in the same category — are there duplicates?
  4. Did the first charge look like a free trial (near zero) before jumping?

For each subscription add these fields:

- is_forgotten: true if last charge >60 days ago AND user probably forgot
- has_price_hike: true if recent amounts are 15%+ higher than earlier amounts
- is_duplicate: true if another subscription in the list serves the same purpose
- is_trial_trap: true if first payment was tiny (<INR 10) then jumped 5x+
- days_since_last_charge: integer
- price_trend: "stable" | "increasing" | "decreasing" | "irregular"
- behaviour_flags: list of emoji flags e.g. ["💤 Forgotten", "📈 Price Hike"]
- behaviour_summary: 1-2 sentence plain English summary of this subscription's pattern

SUBSCRIPTIONS:
{json.dumps(subs, indent=2)}

Return ONLY a JSON array — the same subscriptions with the new fields added.
No markdown, no preamble."""

        result = self.llm.call_json(prompt, max_tokens=4096)
        if isinstance(result, list) and result:
            return result
        # Fallback defaults
        for s in subs:
            s.setdefault("is_forgotten", False)
            s.setdefault("has_price_hike", False)
            s.setdefault("is_duplicate", False)
            s.setdefault("is_trial_trap", False)
            s.setdefault("behaviour_flags", [])
            s.setdefault("behaviour_summary", "Insufficient data.")
        return subs

    # ── 3C: Value Scoring ─────────────────────────────────────
    def score_subscriptions(self, subs: list) -> list:
        """
        Technique: CoT + persona + few-shot + structured output
        LLM reasons about value-for-money for each subscription
        in the context of Indian pricing and alternatives.
        """
        if not subs: return subs

        prompt = f"""{PERSONA}

TASK: Assign a Worth-It Score (0-100) to each subscription.
0 = waste of money, cancel immediately. 100 = essential, definitely keep.

SCORING FRAMEWORK — reason through each factor:
  1. Price vs Indian market norms (INR 649 Netflix = fair, INR 3000 for unused app = not)
  2. Recency signals — forgotten subs score low regardless of price
  3. Behaviour flags — forgotten(-30), price_hike(-15), duplicate(-20), trial_trap(-10)
  4. Category context — fitness subs need active use, OTT ok if used occasionally
  5. Availability of cheaper Indian alternatives

FEW-SHOT EXAMPLE:
Netflix, INR 649/month, 6 months of consistent charges, no flags
→ worth_it_score: 85, score_label: "✅ Keep", reasoning: "Consistent use at fair Indian price. No flags."

Some gym app, INR 2400/month, last charged 90 days ago, flagged as forgotten
→ worth_it_score: 12, score_label: "🚨 Cancel", reasoning: "Forgotten for 3 months. High cost. Cult.fit or local gym cheaper."

SUBSCRIPTIONS:
{json.dumps(subs, indent=2)}

Return ONLY a JSON array with these fields added to each subscription:
- worth_it_score: integer 0-100
- score_label: "🚨 Cancel" if <40, "⚠️ Review" if 40-70, "✅ Keep" if >70
- score_reasoning: 2-3 sentences explaining the score (mention INR amounts)
- cancel_priority: rank 1 = cancel first (1 is highest priority)
- recommended_action: "Cancel" | "Downgrade" | "Keep" | "Review Usage"
- alternative_suggestion: if Cancel/Downgrade, name a specific cheaper Indian alternative

No markdown, no preamble. Just the JSON array."""

        result = self.llm.call_json(prompt, max_tokens=4096)
        return result if isinstance(result, list) and result else subs


