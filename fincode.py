def _rule_based_score(sub: dict) -> dict:
    """
    FALLBACK: Compute worth_it_score purely from behaviour flags and cost.
    Used when the LLM scoring call fails or returns incomplete data.
    """
    score = 60
    flags = sub.get("behaviour_flags", [])
    flag_str = " ".join(flags).lower()

    if sub.get("is_forgotten") or "forgotten" in flag_str:
        score -= 30
    if sub.get("is_duplicate") or "duplicate" in flag_str:
        score -= 20
    if sub.get("has_price_hike") or "price hike" in flag_str:
        score -= 10
    if sub.get("is_trial_trap") or "trial" in flag_str:
        score -= 10

    # Penalise high-cost forgotten subs more
    monthly = sub.get("monthly_cost", 0)
    if sub.get("is_forgotten") and monthly > 1000:
        score -= 15

    score = max(0, min(100, score))

    if score < 40:
        label  = "🚨 Cancel"
        action = "Cancel"
    elif score < 70:
        label  = "⚠️ Review"
        action = "Review Usage"
    else:
        label  = "✅ Keep"
        action = "Keep"

    sub["worth_it_score"]    = score
    sub["score_label"]       = label
    sub["recommended_action"] = action
    sub["score_reasoning"]   = (
        f"Auto-scored: {'forgotten sub — no recent activity. ' if sub.get('is_forgotten') else ''}"
        f"{'Duplicate category detected. ' if sub.get('is_duplicate') else ''}"
        f"INR {monthly:.0f}/month."
    )
    sub["cancel_priority"]       = 0
    sub["alternative_suggestion"] = ""
    return sub


class AICore:

    def __init__(self, llm: LLMClient):
        self.llm = llm

    # ── 3A: Subscription Detection ────────────────────────────
    def detect_subscriptions(self, df: pd.DataFrame) -> list:
        summary = (
            df.groupby("merchant")
            .agg(count=("amount","count"), avg=("amount","mean"),
                 min_amt=("amount","min"), max_amt=("amount","max"),
                 first_date=("date","min"), last_date=("date","max"),
                 all_amounts=("amount", list), all_dates=("date", list))
            .reset_index()
        )

        rows = []
        for _, r in summary.iterrows():
            dates_ts = sorted(r["all_dates"])
            if len(dates_ts) > 1:
                gaps = [(dates_ts[i]-dates_ts[i-1]).days for i in range(1,len(dates_ts))]
                avg_gap = round(sum(gaps)/len(gaps),1)
            else:
                avg_gap = None

            dates_str = [d.strftime("%Y-%m-%d") for d in dates_ts]
            rows.append({
                "merchant":     r["merchant"],
                "count":        int(r["count"]),
                "avg_amount":   round(float(r["avg"]), 2),
                "min_amount":   round(float(r["min_amt"]), 2),
                "max_amount":   round(float(r["max_amt"]), 2),
                "first_date":   dates_str[0],
                "last_date":    dates_str[-1],
                "avg_gap_days": avg_gap,
                "all_amounts":  [round(float(a),2) for a in r["all_amounts"]],
                "all_dates":    dates_str
            })

        all_subs = []
        for i in range(0, len(rows), 10):
            all_subs.extend(self._detect_chunk(rows[i:i+10]))
        return all_subs

    def _detect_chunk(self, chunk: list) -> list:
        prompt = f"""{PERSONA}

TASK: Analyse these merchant transaction summaries. Identify which are RECURRING SUBSCRIPTIONS.

RULES:
- avg_gap_days 25-35 → monthly, 85-95 → quarterly, 350-380 → annual
- Known Indian/global subscription services: always mark as subscription
- Single purchases or irregular shopping: mark is_subscription=false
- Include only subscriptions with confidence >= 25

Return a JSON array. Each element:
{{
  "merchant": "string",
  "is_subscription": true,
  "billing_cycle": "weekly|monthly|quarterly|annual",
  "category": "OTT|SaaS|Fitness|News|Cloud|Food-Delivery|Telecom|Other",
  "avg_amount": 0.0,
  "monthly_cost": 0.0,
  "annual_cost": 0.0,
  "next_renewal": "YYYY-MM-DD",
  "confidence": 0,
  "all_amounts": [],
  "all_dates": []
}}

Include original all_amounts and all_dates arrays in output for downstream analysis.

DATA:
{json.dumps(chunk, indent=2)}"""

        result = self.llm.call_json(prompt, max_tokens=3000)
        if isinstance(result, list):
            # Keep only actual subscriptions
            return [r for r in result if r.get("is_subscription", False)]
        return []

    # ── 3B: Behavioural Analysis ──────────────────────────────
    def analyse_behaviour(self, subs: list) -> list:
        if not subs: return subs

        # Slim payload for LLM
        subs_slim = [{k: v for k, v in s.items()
                      if k in ("merchant","avg_amount","all_amounts","all_dates",
                               "billing_cycle","category","last_date","monthly_cost")}
                     for s in subs]

        prompt = f"""{PERSONA}

TASK: Behavioural analysis on these subscriptions.

For each subscription add EXACTLY these fields:
- is_forgotten: true if last_date is >60 days before today ({datetime.today().strftime('%Y-%m-%d')})
- has_price_hike: true if latest amounts are 15%+ higher than earliest amounts
- is_duplicate: true if another sub in the list covers the same purpose
- is_trial_trap: true if first payment < INR 10, subsequent payments 5x higher
- days_since_last_charge: integer days since last charge
- price_trend: "stable" | "increasing" | "decreasing" | "irregular"
- behaviour_flags: array of strings like ["💤 Forgotten", "📈 Price Hike", "🔁 Duplicate", "🪤 Trial Trap"]
- behaviour_summary: 1 sentence about usage pattern

SUBSCRIPTIONS:
{json.dumps(subs_slim, indent=2)}

Return a JSON array — same order as input, same merchants, with the new fields added.
Do NOT drop any subscriptions from the list."""

        result = self.llm.call_json(prompt, max_tokens=3000)

        if isinstance(result, list) and len(result) == len(subs):
            # Merge new fields back into original subs (preserve all original fields)
            merged = []
            for orig, enriched in zip(subs, result):
                merged_sub = {**orig}  # start with ALL original fields
                for key in ("is_forgotten","has_price_hike","is_duplicate","is_trial_trap",
                            "days_since_last_charge","price_trend","behaviour_flags","behaviour_summary"):
                    if key in enriched:
                        merged_sub[key] = enriched[key]
                merged.append(merged_sub)
            return merged

        # Partial match by merchant name
        if isinstance(result, list) and result:
            enriched_map = {r.get("merchant","").lower(): r for r in result}
            for sub in subs:
                key = sub.get("merchant","").lower()
                if key in enriched_map:
                    for field in ("is_forgotten","has_price_hike","is_duplicate","is_trial_trap",
                                  "days_since_last_charge","price_trend","behaviour_flags","behaviour_summary"):
                        if field in enriched_map[key]:
                            sub[field] = enriched_map[key][field]

        # Apply rule-based fallback for missing fields
        today = datetime.today()
        for sub in subs:
            sub.setdefault("behaviour_flags", [])
            sub.setdefault("price_trend", "stable")
            sub.setdefault("behaviour_summary", "Insufficient data for pattern analysis.")
            sub.setdefault("is_forgotten", False)
            sub.setdefault("has_price_hike", False)
            sub.setdefault("is_duplicate", False)
            sub.setdefault("is_trial_trap", False)

            # Rule-based is_forgotten fallback
            try:
                last = datetime.strptime(sub.get("last_date",""), "%Y-%m-%d")
                days = (today - last).days
                sub["days_since_last_charge"] = days
                if days > 60 and not sub.get("is_forgotten"):
                    sub["is_forgotten"] = True
                    sub["behaviour_flags"].append("💤 Forgotten")
                    sub["behaviour_summary"] = f"Last charged {days} days ago — possibly forgotten."
            except Exception:
                sub.setdefault("days_since_last_charge", 0)

        return subs

    # ── 3C: Value Scoring ─────────────────────────────────────
    def score_subscriptions(self, subs: list) -> list:
        if not subs: return subs

        subs_slim = [{k: v for k, v in s.items()
                      if k not in ("all_amounts","all_dates")}
                     for s in subs]

        prompt = f"""{PERSONA}

TASK: Assign a Worth-It Score (0-100) to each subscription.
0 = waste of money/cancel immediately. 100 = essential/definitely keep.

SCORING LOGIC:
- Forgotten sub (is_forgotten=true): subtract 30 points
- Duplicate category (is_duplicate=true): subtract 20 points
- Price hike (has_price_hike=true): subtract 10 points
- Trial trap: subtract 10 points
- High cost (>INR 2000/month) with forgotten flag: subtract extra 15 points
- Known essential service actively used: base 75+

Return a JSON array. Preserve ALL input fields and ADD:
- worth_it_score: integer 0-100
- score_label: exactly one of "🚨 Cancel" (score<40) | "⚠️ Review" (40-70) | "✅ Keep" (score>70)
- score_reasoning: 2 sentences with INR amounts
- cancel_priority: 1=cancel first (lower score = higher priority)
- recommended_action: "Cancel" | "Downgrade" | "Keep" | "Review Usage"
- alternative_suggestion: if Cancel/Downgrade, name a cheaper Indian alternative

SUBSCRIPTIONS:
{json.dumps(subs_slim, indent=2)}

IMPORTANT: Return ALL {len(subs)} subscriptions in the same order."""

        result = self.llm.call_json(prompt, max_tokens=3000)

        if isinstance(result, list) and len(result) == len(subs):
            # Merge back — keep all original fields, overlay scored fields
            scored = []
            for orig, scored_sub in zip(subs, result):
                merged = {**orig, **scored_sub}  # scored_sub fields take precedence
                # Ensure score_label matches worth_it_score
                ws = merged.get("worth_it_score", 50)
                if isinstance(ws, (int, float)):
                    if ws < 40: merged["score_label"] = "🚨 Cancel"
                    elif ws < 70: merged["score_label"] = "⚠️ Review"
                    else: merged["score_label"] = "✅ Keep"
                scored.append(merged)
            return scored

        # Partial match by merchant
        if isinstance(result, list) and result:
            scored_map = {r.get("merchant","").lower(): r for r in result}
            for sub in subs:
                key = sub.get("merchant","").lower()
                if key in scored_map:
                    sub.update(scored_map[key])
                    ws = sub.get("worth_it_score", 50)
                    if isinstance(ws, (int, float)):
                        if ws < 40: sub["score_label"] = "🚨 Cancel"
                        elif ws < 70: sub["score_label"] = "⚠️ Review"
                        else: sub["score_label"] = "✅ Keep"

        # Full rule-based fallback for any sub missing worth_it_score
        for sub in subs:
            if "worth_it_score" not in sub or sub.get("worth_it_score") is None:
                _rule_based_score(sub)

        # Assign cancel_priority based on score
        cancel_subs = sorted(
            [(i, s) for i, s in enumerate(subs) if s.get("worth_it_score", 100) < 40],
            key=lambda x: x[1].get("worth_it_score", 100)
        )
        for rank, (i, _) in enumerate(cancel_subs, 1):
            subs[i]["cancel_priority"] = rank

        return subs


