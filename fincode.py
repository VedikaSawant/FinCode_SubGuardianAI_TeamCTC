class PDFParser:
    """
    Multi-format PDF parser.
    Includes a dedicated PhonePe parser (primary) that handles the
    AM/PM line-split quirk in pdfplumber output, plus generic
    fallback patterns for HDFC, SBI, Axis, GPay, Paytm.
    """
    DATE_FMTS = ["%b %d, %Y %I:%M %p", "%d %b %Y", "%d-%b-%Y",
                 "%d/%m/%Y", "%b %d, %Y", "%Y-%m-%d", "%d %b %y"]

    # Generic fallback patterns for non-PhonePe statements
    GENERIC_PATTERNS = [
        r"(\d{1,2}[\s\-/]\w{3}[\s\-/]\d{2,4})\s+(.+?)\s+([\d,]+\.\d{2})\s*(Dr|Cr)?",
        r"(\d{2}/\d{2}/\d{4})\s+(.+?)\s+([\d,]+\.\d{2})\s*(Dr|Cr)?",
        r"(\d{4}-\d{2}-\d{2})\s+(.+?)\s+[-]?([\d,]+\.\d{2})",
    ]

    def parse(self, path: str) -> pd.DataFrame:
        rows = []
        try:
            with pdfplumber.open(path) as pdf:
                # Concatenate all pages into one string for full-doc regex
                full_text = ""
                for page in pdf.pages:
                    full_text += (page.extract_text() or "") + "\n"

                # ── PhonePe primary pattern ────────────────────────────
                # KEY FIX: subscriptions charge at 08:00 AM. pdfplumber wraps
                # "AM" to the next line AFTER transaction details, so AM/PM
                # must be OPTIONAL in the date group.
                # Format per line:
                #   "{Month} {dd}, {yyyy} {HH:MM}[ AM/PM] Paid to {merchant}
                #    Transaction ID: {tid} UTR Debit/Credit INR {amount}"
                phonepe_pat = re.compile(
                    r"(\w{3}\s+\d{2},\s+\d{4}\s+\d{1,2}:\d{2})"  # date+time
                    r"(?:\s+(?:AM|PM))?"                                   # AM/PM optional
                    r"\s+(?:Paid to|Received from)\s+"
                    r"(.+?)"
                    r"\s+Transaction ID:.*?"
                    r"(Debit|Credit)"
                    r"\s+INR\s+([\d,]+\.\d{2})",
                    re.DOTALL
                )
                for m in phonepe_pat.finditer(full_text):
                    date_str, merchant, txn_type, amount_str = m.groups()
                    if txn_type != "Debit":
                        continue
                    try:
                        # Parse without AM/PM (24h) — time is not critical for analysis
                        date = datetime.strptime(date_str.strip(), "%b %d, %Y %H:%M")
                        amt  = float(amount_str.replace(",", ""))
                        if amt > 0:
                            rows.append({
                                "date":     date,
                                "merchant": merchant.strip()[:80],
                                "amount":   amt,
                                "month":    date.strftime("%b %Y")
                            })
                    except Exception:
                        pass

                # ── Generic fallback (runs per page) ──────────────────
                if not rows:
                    for page in pdf.pages:
                        text = page.extract_text() or ""
                        for pat in self.GENERIC_PATTERNS:
                            for m in re.finditer(pat, text, re.IGNORECASE | re.DOTALL):
                                r = self._from_generic_match(m)
                                if r: rows.append(r)
                        for tbl in (page.extract_tables() or []):
                            rows += self._from_table(tbl)

        except Exception as e:
            print(f"[PDF] {e}")

        if not rows:
            return pd.DataFrame()
        df = pd.DataFrame(rows).drop_duplicates(subset=["date","merchant","amount"])
        print(f"[PDF] Extracted {len(df)} debit transactions")
        return df

    def _from_generic_match(self, m):
        try:
            g = m.groups()
            date = self._parse_date(g[0])
            if not date: return None
            amt_str = next((x for x in g[1:] if x and re.match(r"[\d,]+\.?\d*$", x.replace(",",""))), None)
            if not amt_str: return None
            amt  = float(amt_str.replace(",",""))
            desc = g[1].strip() if g[1] != amt_str else (g[2].strip() if len(g) > 2 else "")
            if amt <= 0 or not desc: return None
            return {"date": date, "merchant": desc[:80],
                    "amount": amt, "month": date.strftime("%b %Y")}
        except Exception:
            return None

    def _from_table(self, table):
        rows = []
        for row in table:
            if not row or len(row) < 3: continue
            cells = [str(c or "").strip() for c in row]
            di = next((i for i, c in enumerate(cells) if self._parse_date(c)), None)
            ai = next((i for i, c in enumerate(cells)
                       if re.match(r"^[\d,]+\.?\d*$", c.replace(",",""))), None)
            if di is None or ai is None: continue
            desc = " ".join(cells[i] for i in range(len(cells)) if i not in (di, ai) and cells[i])[:60]
            date = self._parse_date(cells[di])
            try:
                amt = float(cells[ai].replace(",",""))
                if amt > 0 and date:
                    rows.append({"date": date, "merchant": desc,
                                 "amount": amt, "month": date.strftime("%b %Y")})
            except ValueError:
                pass
        return rows

    def _parse_date(self, s):
        s = re.sub(r"\s+", " ", s.strip())
        for fmt in self.DATE_FMTS:
            try: return datetime.strptime(s, fmt)
            except ValueError: pass
        return None


class DataNormalizer:
    """Canonical merchant name resolution using alias map + RapidFuzz fallback."""
    ALIASES = {
        "netflix":          ["netflix", "nflx", "netflix india"],
        "spotify":          ["spotify", "spotify ab", "spotify india"],
        "youtube premium":  ["youtube premium", "google youtube", "yt premium"],
        "disney+ hotstar":  ["disney+ hotstar", "hotstar", "jiocinema"],
        "amazon prime":     ["amazon prime", "prime video", "amzn prime"],
        "adobe":            ["adobe", "adobe systems", "adobe creative cloud"],
        "canva":            ["canva pty", "canva india", "canva"],
        "notion":           ["notion labs", "notion.so", "notion"],
        "cult.fit":         ["cult.fit", "cure.fit", "cultfit"],
        "healthifyme":      ["healthifyme", "healthify me"],
        "zomato":           ["zomato pro", "zomato gold", "zomato"],
        "swiggy":           ["swiggy one", "swiggy instamart", "swiggy"],
        "google one":       ["google one", "google storage"],
        "icloud":           ["apple icloud", "icloud", "apple storage"],
        "airtel":           ["airtel postpaid", "airtel xstream", "airtel"],
        "jio":              ["reliance jio", "jio fiber", "jio"],
        "microsoft 365":    ["microsoft 365", "ms office", "office 365"],
        "zee5":             ["zee5", "zee entertainment"],
        "sonyliv":          ["sonyliv", "sony liv"],
    }

    def __init__(self):
        self._map = {a.lower(): c for c, aliases in self.ALIASES.items() for a in aliases}

    def normalize(self, df: pd.DataFrame) -> pd.DataFrame:
        if df.empty: return df
        df = df.copy()
        df["merchant"] = df["merchant"].apply(self._norm)
        df["amount"]   = pd.to_numeric(df["amount"], errors="coerce").fillna(0)
        df["date"]     = pd.to_datetime(df["date"], errors="coerce")
        df             = df.dropna(subset=["date","amount"])
        df["month"]    = df["date"].dt.strftime("%b %Y")
        return df.sort_values("date").reset_index(drop=True)

    def _norm(self, name):
        clean = re.sub(r"[^a-z0-9\s\.]", "", name.lower()).strip()
        if clean in self._map: return self._map[clean]
        hit = process.extractOne(clean, list(self._map.keys()),
                                 scorer=fuzz.partial_ratio, score_cutoff=82)
        return self._map[hit[0]] if hit else clean.title()


class NoiseFilter:
    """Removes salary, P2P UPI, EMI, ATM, tax, refund transactions."""
    BLOCK = ["transfer to","sent to","upi/","neft/","rtgs/","imps/",
             "salary","payroll","wages","emi","loan","atm withdrawal",
             "cash deposit","refund","cashback","tax","gst","fd deposit"]

    def filter(self, df: pd.DataFrame) -> pd.DataFrame:
        if df.empty: return df
        mask = df["merchant"].str.lower().apply(
            lambda x: not any(kw in x for kw in self.BLOCK))
        return df[mask].reset_index(drop=True)


def parse_manual(text: str) -> pd.DataFrame:
    rows = []
    for line in (text or "").strip().split("\n"):
        parts = [p.strip() for p in line.split(",")]
        if len(parts) >= 2:
            try:
                rows.append({"merchant": parts[0], "amount": float(parts[1]),
                              "month": datetime.today().strftime("%b %Y"),
                              "date": datetime.today()})
            except ValueError:
                pass
    return pd.DataFrame(rows)


