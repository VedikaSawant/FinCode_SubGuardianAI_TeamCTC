def run_audit(bank_pdf, upi_pdf, manual_text, progress=gr.Progress()):
    """Generator function — yields progress updates while running the pipeline."""

    progress(0.05, desc="📄 Reading your financial data...")
    yield (
        "### 🧠 Reading your financial behaviour...",
        "", None, None, None, None, None, None
    )

    # ── Ingest ────────────────────────────────────────────────
    frames = []
    for path in filter(None, [bank_pdf.name if bank_pdf else None,
                               upi_pdf.name if upi_pdf else None]):
        frames.append(pipeline.parser.parse(path))

    manual = parse_manual(manual_text or "")
    if not manual.empty:
        frames.append(manual)

    if not frames:
        yield "❌ No input provided. Upload a PDF or enter manual data.", "", None, None, None, None, None, None
        return

    raw     = pd.concat(frames, ignore_index=True)
    cleaned = pipeline.filter.filter(pipeline.norm.normalize(raw))

    if cleaned.empty:
        yield "❌ No transactions found after cleaning.", "", None, None, None, None, None, None
        return

    # ── Step 1: Detect ────────────────────────────────────────
    progress(0.25, desc="🔎 Detecting recurring subscriptions...")
    yield "### 🔎 Detecting recurring subscriptions...", "", None, None, None, None, None, None

    subs = pipeline.ai.detect_subscriptions(cleaned)

    if not subs:
        yield "❌ No subscriptions detected. Try manual entry (e.g. Netflix, 649, monthly).", "", None, None, None, None, None, None
        return

    print(f"[PIPELINE] Detected {len(subs)} subscriptions")

    # ── Step 2: Behaviour ─────────────────────────────────────
    progress(0.45, desc="📊 Analysing usage patterns & flags...")
    yield f"### 📊 Analysing {len(subs)} subscriptions for behaviour patterns...", "", None, None, None, None, None, None

    subs = pipeline.ai.analyse_behaviour(subs)
    print(f"[PIPELINE] Behaviour flags applied")

    # ── Step 3: Scoring ───────────────────────────────────────
    progress(0.65, desc="⚖️ Evaluating value for money...")
    yield "### ⚖️ Evaluating worth-it scores & savings opportunities...", "", None, None, None, None, None, None

    subs = pipeline.ai.score_subscriptions(subs)
    print(f"[PIPELINE] Scoring complete. Sample: {[(s.get('merchant'), s.get('worth_it_score')) for s in subs[:3]]}")

    # ── Step 4: Analytics ─────────────────────────────────────
    progress(0.80, desc="💸 Computing financial impact...")
    yield "### 💸 Calculating financial impact & projections...", "", None, None, None, None, None, None

    leakage = pipeline.analytics.compute_leakage(subs)
    savings = pipeline.analytics.project_savings(subs)
    print(f"[PIPELINE] Leakage: {leakage}")
    print(f"[PIPELINE] Savings: {savings}")

    # ── Step 5: Insights ──────────────────────────────────────
    progress(0.90, desc="💡 Generating personalised AI insights...")
    yield "### 💡 Generating personalised insights & cancellation guides...", "", None, None, None, None, None, None

    insights = pipeline.analytics.llm_insights(subs, leakage, savings)
    alerts   = pipeline.alerter.generate(subs)

    # ── Step 6: Report ────────────────────────────────────────
    progress(0.97, desc="📄 Generating PDF report...")
    yield "### 📄 Generating PDF audit report...", "", None, None, None, None, None, None

    report = pipeline.reporter.generate(leakage, subs, savings, insights)

    # ── Build dashboard ───────────────────────────────────────
    progress(1.0, desc="✅ Analysis complete!")
    dashboard_html, vault_df, ahtml, fig_cat, fig_scatter, action_html, report_file = \
        build_dashboard(subs, leakage, savings, insights, alerts, report)

    yield (
        "### ✅ Analysis complete! 🎉",
        dashboard_html, vault_df, ahtml, fig_cat, fig_scatter, action_html, report_file
    )




