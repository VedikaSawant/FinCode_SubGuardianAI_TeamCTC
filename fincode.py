class AlertEngine:

    def __init__(self, llm: LLMClient):
        self.llm = llm

    def generate(self, subs: list) -> list:
        raw = self._detect(subs)
        if not raw: return []

        prompt = f"""{PERSONA}

Rewrite these alert messages to be clear, urgent and friendly for Indian users.
Keep each message under 25 words. Mention INR amounts.

ALERTS: {json.dumps(raw)}

Return ONLY a JSON array with the same structure but improved "message" fields."""

        result = self.llm.call_json(prompt, max_tokens=800)
        return result if isinstance(result, list) and result else raw

    def _detect(self, subs: list) -> list:
        alerts = []
        today  = datetime.today()
        for s in subs:
            try:
                days = (datetime.strptime(s.get("next_renewal",""), "%Y-%m-%d") - today).days
                if 0 <= days <= 7:
                    alerts.append({
                        "type": "🔔 Upcoming Renewal", "merchant": s["merchant"],
                        "message": f"Renews in {days} days — INR {s.get('monthly_cost',0):.0f}",
                        "urgency": "high" if days <= 3 else "medium",
                    })
            except (ValueError, TypeError):
                pass
            if s.get("is_forgotten"):
                alerts.append({
                    "type": "💤 Forgotten", "merchant": s["merchant"],
                    "message": f"Still paying INR {s.get('monthly_cost',0):.0f}/month for {s['merchant']} — used it recently?",
                    "urgency": "high",
                })
            if s.get("has_price_hike"):
                alerts.append({
                    "type": "📈 Price Hike", "merchant": s["merchant"],
                    "message": f"{s['merchant']} recently increased its price — review plan.",
                    "urgency": "medium",
                })
            if s.get("is_duplicate"):
                alerts.append({
                    "type": "🔁 Duplicate", "merchant": s["merchant"],
                    "message": f"{s['merchant']} overlaps with another subscription — consider cancelling.",
                    "urgency": "medium",
                })
        return sorted(alerts, key=lambda x: x["urgency"]=="high", reverse=True)





