def send_alert_email(
    sender_email: str,
    sender_password: str,
    recipient_email: str,
    recipient_name: str,
    alerts: list,
    subs: list,
    leakage: dict,
    savings: dict,
    attach_pdf: str = None,
) -> tuple[bool, str]:
    """
    Send a SubGuard alert email via Gmail SMTP (SSL).
    Returns (success: bool, message: str).

    For Gmail: use an App Password (not your account password).
    Generate one at: https://myaccount.google.com/apppasswords
    """
    if not sender_email or not sender_password or not recipient_email:
        return False, "Please fill in all email fields."

    if "@" not in recipient_email or "@" not in sender_email:
        return False, "Invalid email address format."

    try:
        html_body = build_alert_email_html(alerts, subs, leakage, savings, recipient_name)

        msg = MIMEMultipart("alternative")
        msg["Subject"] = (
            f"🛡️ SubGuard Alert — "
            f"₹{leakage.get('monthly_total',0):,.0f}/mo drain detected | "
            f"{leakage.get('cancel_count',0)} subscriptions to cancel"
        )
        msg["From"]    = f"SubGuard AI <{sender_email}>"
        msg["To"]      = recipient_email

        # Plain text fallback
        plain_lines = [
            "SubGuard AI — Subscription Alert Reminder",
            "=" * 45,
            f"Monthly Drain:  ₹{leakage.get('monthly_total',0):,.0f}",
            f"Annual Drain:   ₹{leakage.get('annual_total',0):,.0f}",
            f"Potential Save: ₹{savings.get('monthly_savings',0):,.0f}/mo",
            f"Health Score:   {leakage.get('health_score',0):.0f}/100",
            "",
            "ALERTS:",
        ]
        for a in alerts:
            plain_lines.append(
                f"  [{a.get('urgency','').upper()}] {a.get('type','')} "
                f"{a.get('merchant','')} — {a.get('message','')}"
            )
        plain_lines += ["", "SubGuard AI | Team CTC | FinCode Hackathon"]

        msg.attach(MIMEText("\n".join(plain_lines), "plain"))
        msg.attach(MIMEText(html_body, "html"))

        # Attach PDF report if provided
        if attach_pdf and os.path.exists(attach_pdf):
            with open(attach_pdf, "rb") as f:
                part = MIMEBase("application", "octet-stream")
                part.set_payload(f.read())
            encoders.encode_base64(part)
            part.add_header(
                "Content-Disposition",
                f"attachment; filename=SubGuard_Report.pdf"
            )
            msg.attach(part)

        # Send via Gmail SSL
        with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
            server.login(sender_email, sender_password)
            server.sendmail(sender_email, recipient_email, msg.as_string())

        return True, f"✅ Alert email sent to {recipient_email} successfully!"

    except smtplib.SMTPAuthenticationError:
        return False, (
            "❌ Authentication failed. "
            "For Gmail, use an App Password (not your account password). "
            "Generate at: myaccount.google.com/apppasswords"
        )
    except smtplib.SMTPException as e:
        return False, f"❌ SMTP error: {str(e)}"
    except Exception as e:
        return False, f"❌ Error sending email: {str(e)}"


