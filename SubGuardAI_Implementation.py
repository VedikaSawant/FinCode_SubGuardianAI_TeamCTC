class AnalyticsEngine:
    def compute_leakage(self, subs: list) -> dict:
        if not subs: return {}
        monthly = sum(s.get("monthly_cost", 0) for s in subs)
        annual  = sum(s.get("annual_cost",  0) for s in subs)
        cats    = {}
        for s in subs:
            c = s.get("category", "Other")
            cats[c] = cats.get(c, 0) + s.get("monthly_cost", 0)
        forgotten_ct = sum(1 for s in subs if s.get("is_forgotten"))
        duplicate_ct = sum(1 for s in subs if s.get("is_duplicate"))
        pricehike_ct = sum(1 for s in subs if s.get("has_price_hike"))
        trialtrap_ct = sum(1 for s in subs if s.get("is_trial_trap"))
        cancel_ct    = sum(1 for s in subs if
                           s.get("recommended_action","").lower() == "cancel" or
                           s.get("worth_it_score", 100) < 55)
        wasted       = sum(s.get("monthly_cost", 0) for s in subs if
                           s.get("recommended_action","").lower() == "cancel" or
                           s.get("worth_it_score", 100) < 55)

        if   monthly <= 1000: d1 = 40
        elif monthly <= 1800: d1 = 30
        elif monthly <= 3500: d1 = 18
        elif monthly <= 6000: d1 = 8
        else:                 d1 = 0

        d2 = 35
        d2 -= min(forgotten_ct * 10, 35)
        d2 -= min(duplicate_ct * 7,  21)
        d2  = max(d2, 0)

        d3 = 25
        d3 -= min(cancel_ct    * 8, 24)
        d3 -= min(pricehike_ct * 5, 15)
        d3 -= min(trialtrap_ct * 7, 14)
        d3  = max(d3, 0)

        health = round(float(d1 + d2 + d3), 1)
        return {
            "monthly_total":   round(monthly, 2),
            "annual_total":    round(annual, 2),
            "wasted_monthly":  round(wasted, 2),
            "cat_breakdown":   {k: round(v, 2) for k, v in sorted(cats.items(), key=lambda x: -x[1])},
            "health_score":    health,
            "health_dim1":     d1,
            "health_dim2":     d2,
            "health_dim3":     d3,
            "health_dim1_max": 40,
            "health_dim2_max": 35,
            "health_dim3_max": 25,
            "sub_count":       len(subs),
            "cancel_count":    cancel_ct,
            "forgotten_count": forgotten_ct,
        }

    def project_savings(self, subs: list) -> dict:
        cancel = [s for s in subs if
                  s.get("recommended_action","").lower() == "cancel" or
                  s.get("worth_it_score", 100) < 55]
        ms  = sum(s.get("monthly_cost", 0) for s in cancel)
        ans = sum(s.get("annual_cost",  0) for s in cancel)
        r   = 0.12 / 12
        return {
            "monthly_savings": round(ms, 2),
            "annual_savings":  round(ans, 2),
            "sip_5yr_value":   round(abs(npf.fv(r, 60,  -ms, 0)) if ms else 0, 2),
            "sip_10yr_value":  round(abs(npf.fv(r, 120, -ms, 0)) if ms else 0, 2),
            "cancel_count":    len(cancel),
            "top3_to_cancel":  sorted(cancel, key=lambda x: -x.get("annual_cost",0))[:3],
        }


