class LLMClient:
    MODEL   = "gemini-2.5-flash-lite"
    BACKUP  = "gemini-1.5-flash"
    API_URL = (
        "https://generativelanguage.googleapis.com/v1beta/models/"
        "{model}:generateContent?key={key}"
    )

    def __init__(self):
        self._key = os.environ.get("GEMINI_API_KEY", "")
        self._url = self.API_URL.format(model=self.MODEL, key=self._key)

    def call(self, prompt: str, max_tokens: int = 4096, retries: int = 5) -> str:
        payload = {
            "contents": [{"parts": [{"text": prompt}]}],
            "generationConfig": {"temperature": 0.2, "maxOutputTokens": min(max_tokens, 8192)}
        }
        headers = {"Content-Type": "application/json"}

        for attempt in range(retries):
            try:
                r = _requests.post(self._url, json=payload, headers=headers, timeout=120)
                if r.status_code == 200:
                    data = r.json()
                    return (data["candidates"][0]["content"]["parts"][0]["text"]
                            if data.get("candidates") else "")
                if r.status_code in (429, 503):
                    wait   = (2 ** attempt) * 20
                    reason = "rate limit" if r.status_code == 429 else "service overloaded"
                    print(f"[Gemini] {r.status_code} {reason} — waiting {wait}s "
                          f"(attempt {attempt+1}/{retries})")
                    time.sleep(wait)
                    continue
                print(f"[Gemini Error] HTTP {r.status_code}: {r.text[:200]}")
                return ""
            except Exception as e:
                print(f"[Gemini Exception] {e}")
                time.sleep(10)

        print("[Gemini] All retries exhausted. Trying backup model...")
        backup_url = self.API_URL.format(model=self.BACKUP, key=self._key)
        try:
            r = _requests.post(backup_url, json=payload, headers=headers, timeout=120)
            if r.status_code == 200:
                data = r.json()
                return (data["candidates"][0]["content"]["parts"][0]["text"]
                        if data.get("candidates") else "")
        except Exception as e:
            print(f"[Gemini Backup Exception] {e}")

        return ""

    def call_json(self, prompt: str, max_tokens: int = 4096):
        prompt += "\n\nIMPORTANT: Output STRICT VALID JSON ONLY. No markdown fences."
        raw = self.call(prompt, max_tokens)
        raw = re.sub(r"```json\s*", "", raw)
        raw = re.sub(r"```", "", raw).strip()
        try:
            return json.loads(raw)
        except Exception:
            match = re.search(r"(\{.*\}|\[.*\])", raw, re.DOTALL)
            if match:
                try:
                    return json.loads(match.group(1))
                except Exception:
                    pass
        print("[JSON FAIL]", raw[:300])
        return {}


