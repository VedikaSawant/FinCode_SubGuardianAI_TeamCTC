class DrainGuardPipeline:
    def __init__(self):
        self.llm       = LLMClient()
        self.parser    = PDFParser()
        self.norm      = DataNormalizer()
        self.filter    = NoiseFilter()
        self.ai        = AICore(self.llm)
        self.analytics = AnalyticsEngine()
        self.reporter  = ReportGenerator()

    def run(self, bank_pdf=None, upi_pdf=None, manual_text="",
            city_tier="Tier 1 Metro") -> dict:

        frames = []
        for path in filter(None, [bank_pdf, upi_pdf]):
            frames.append(self.parser.parse(path))
        manual = parse_manual(manual_text)
        if not manual.empty:
            frames.append(manual)

        if not frames:
            return {"error": "No input provided."}

        raw     = pd.concat(frames, ignore_index=True)
        cleaned = self.filter.filter(self.norm.normalize(raw))

        if cleaned.empty:
            return {"error": "No transactions found after cleaning."}

        ai_result = self.ai.run_mega_call(cleaned)
        if not ai_result or not ai_result.get("subscriptions"):
            return {"error": "No subscriptions detected. Try manual entry."}

        seen = set()
        subs_raw = ai_result["subscriptions"]
        subs = []
        for s in subs_raw:
            key = s.get("merchant","").lower().strip()
            if key and key not in seen:
                seen.add(key)
                s["merchant"] = s["merchant"].title()
                subs.append(s)

        insights = ai_result.get("insights", {
            "health_narrative":    "Audit complete.",
            "peer_insight":        "Comparison unavailable.",
            "cohort_label":        "Average Urban Indian",
            "nudges":              [],
            "report_paragraph":    "Subscription audit completed.",
            "cancellation_guides": []
        })
        alerts = ai_result.get("alerts", [])

        leakage  = self.analytics.compute_leakage(subs)
        savings  = self.analytics.project_savings(subs)
        report   = self.reporter.generate(leakage, subs, savings, insights)

        telecom_redundancy = detect_telecom_redundancy(subs)
        seasonal_flags     = detect_seasonal_subscriptions(subs, cleaned)
        peer               = compute_peer_percentile(leakage.get("monthly_total", 0), city_tier)

        return {
            "subs": subs, "raw": cleaned,
            "leakage": leakage, "savings": savings,
            "insights": insights, "alerts": alerts, "report": report,
            "telecom_redundancy": telecom_redundancy,
            "seasonal_flags":     seasonal_flags,
            "peer":               peer,
        }


