def compute_peer_percentile(monthly_total: float, city_tier: str = "Tier 1 Metro") -> dict:
    bench = CITY_TIER_BENCHMARKS.get(city_tier, CITY_TIER_BENCHMARKS["Tier 1 Metro"])
    median = bench["median"]
    if monthly_total <= 0:              percentile = 0
    elif monthly_total <= median * 0.5: percentile = 10
    elif monthly_total <= median:       percentile = 35
    elif monthly_total <= bench["p75"]: percentile = 60
    elif monthly_total <= bench["p90"]: percentile = 80
    elif monthly_total <= bench["p95"]: percentile = 92
    else:                               percentile = 97

    if   percentile >= 95: badge = "🏆 Top 5% Spender";  badge_color = "#EF4444"
    elif percentile >= 90: badge = "🔴 Top 10% Spender"; badge_color = "#F97316"
    elif percentile >= 75: badge = "🟠 Top 25% Spender"; badge_color = "#F59E0B"
    elif percentile >= 50: badge = "🟡 Above Average";   badge_color = "#EAB308"
    elif percentile >= 25: badge = "🟢 Average";         badge_color = "#22C55E"
    else:                  badge = "💚 Budget Champion"; badge_color = "#10B981"

    diff = monthly_total - median
    return {
        "percentile":        percentile,
        "badge":             badge,
        "badge_color":       badge_color,
        "city_tier":         city_tier,
        "city_median":       median,
        "pct_above_median":  round((diff / median) * 100, 1) if monthly_total > median else 0,
        "p75":               bench["p75"],
        "p90":               bench["p90"],
        "cities_example":    bench["cities"],
    }


